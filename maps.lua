Maps1 = Object:extend()

-- load different tileset to create map
function tileset()
	tileset_dirt = love.graphics.newImage("img/maps/dirt.png")
	tileset_grass = love.graphics.newImage("img/maps/grass.png")
	tileset_watergrass = love.graphics.newImage("img/maps/watergrass.png")
end

function drawMaps(x)
    -- Generate first layer of dirt
    for i,row in ipairs(mapstable[x].tilemap_dirt2) do
        for j,tile in ipairs(row) do
            if tile ~= 0 then
                --Draw the image with the correct quad
                love.graphics.draw(tileset_dirt, quads[tile], (j-1) * tilewidth, (i-1) * tileheight)
            end 
        end
    end

    -- Generate Grass
    for i,row in ipairs(mapstable[x].tilemap_grass) do
        for j,tile in ipairs(row) do
            if tile ~= 0 then
                --Draw the image with the correct quad
                love.graphics.draw(tileset_grass, quads[tile], (j-1) * tilewidth, (i-1) * tileheight)
            end 
        end
    end

    -- Generate second layer of dirt
     for i,row in ipairs(mapstable[x].tilemap_dirt1) do
        for j,tile in ipairs(row) do
            if tile ~= 0 then
                --Draw the image with the correct quad
                love.graphics.draw(tileset_dirt, quads[tile], (j-1) * tilewidth, (i-1) * tileheight)
            end 
        end
    end

    -- Generate Water
    for i,row in ipairs(mapstable[x].tilemap_water) do
        for j,tile in ipairs(row) do
            if tile ~= 0 then
                --Draw the image with the correct quad -- quads[tile]
                love.graphics.draw(tileset_watergrass, quads[tile], (j-1) * tilewidth, (i-1) * tileheight)
            end 
        end
    end
end

-- create a quads of the tile set in a common array, all tileset need to be same dimension
function tilesetquads()
	local image_width = 96
    local image_height = 192
    tilewidth = 32 - 2
    tileheight = 32 - 2

    quads = {}

    for i=0,5 do
        for j=0,2 do
            --The only reason this code is split up in multiple lines
            --is so that it fits the page
            table.insert(quads, love.graphics.newQuad(
                    1 + j * (tilewidth + 2),
                    1 + i * (tileheight + 2),
                    tilewidth, tileheight,
                    image_width, image_height))
        end
    end

    return quads
end 

